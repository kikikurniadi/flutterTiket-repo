class Event {
  final String title;
  final String image;
  final String date;
  final String location;
  final String description;
  final String category;
  final double price;

  Event({
    required this.title,
    required this.image,
    required this.date,
    required this.location,
    required this.description,
    required this.category,
    required this.price,
  });
}